def save_graph_visualization(output_path: str = "graph.png") -> None:
    """Generate and save a visualization of the storybook workflow graph."""
    try:
        # Create custom node styles
        node_styles = NodeStyles(
            supervisor={"fill": "#c4a7e7", "stroke": "#6e6a86"},
            research={"fill": "#ebbcba", "stroke": "#6e6a86"},
            writing={"fill": "#9ccfd8", "stroke": "#6e6a86"},
            editorial={"fill": "#f6c177", "stroke": "#6e6a86"}
        )

        # Generate the PNG
        png_data = storybook.get_graph().draw_mermaid_png(
            draw_method=MermaidDrawMethod.API,
            curve_style=CurveStyle.CURVED,
            node_styles=node_styles
        )

        # Ensure output directory exists
        output_dir = os.path.dirname(os.path.abspath(output_path))
        Path(output_dir).mkdir(parents=True, exist_ok=True)

        # Save the PNG
        with open(output_path, "wb") as f:
            f.write(png_data)
        
        logger.info(f"Graph visualization saved to {output_path}")
    except Exception as e:
        logger.error(f"Failed to generate graph visualization: {e}")

