class StorybookState(TypedDict):
    """State definition for the Storybook workflow."""
    # Input fields (required at start)
    manuscript: str  # Required: The raw manuscript text
    title: str  # Required: The manuscript title
    synopsis: str  # Required: Brief synopsis of the work

    # Project management fields (owned by supervisor)
    manuscript_id: Optional[str]  # Generated by supervisor after intake
    status: Optional[str]  # Current workflow status
    current_team: Optional[str]  # Current active team
    stage_progress: Optional[Dict[str, float]]  # Progress tracking

    # Research team outputs
    target_audience: Optional[Dict[str, Any]]  # Set by market research
    research_insights: Optional[Dict[str, Any]]  # Market and genre research
    analysis_results: Optional[Dict[str, Any]]  # Content analysis results

    # Writing team outputs
    settings: Optional[List[Dict[str, Any]]]  # World building results
    characters: Optional[List[Dict[str, Any]]]  # Character development
    subplots: Optional[List[Dict[str, Any]]]  # Subplot integration
    draft_version: Optional[int]  # Current draft number
    
    # Editorial team outputs
    story_analysis: Optional[Dict[str, Any]]  # Story arc evaluation
    continuity_issues: Optional[List[Dict[str, Any]]]  # Continuity checks
    style_analysis: Optional[Dict[str, Any]]  # Language polish results
    final_review: Optional[Dict[str, Any]]  # Quality review results

    # Team status tracking
    research_status: Optional[Dict[str, bool]]  # Research team progress
    writing_status: Optional[Dict[str, bool]]  # Writing team progress
    editorial_status: Optional[Dict[str, bool]]  # Editorial team progress


def start_workflow(state: Dict[str, Any]) -> Dict[str, Any]:
    """Start the workflow with a new manuscript."""
    logger.info("Starting workflow")

    # Validate required input fields
    required_fields = ["manuscript", "title", "synopsis"]
    missing_fields = [f for f in required_fields if not state.get(f)]
    if missing_fields:
        return {
            "status": "error",
            "message": f"Error: {', '.join(missing_fields)} required to start the process."
        }

    # Initialize document store
    document_store = DocumentStore()
    
    # Organize manuscript data
    manuscript_data = {
        "content": state["manuscript"],
        "title": state["title"],
        "synopsis": state["synopsis"],
        "metadata": {
            "created_at": datetime.now().isoformat(),
            "word_count": len(state["manuscript"].split()),
        }
    }
    
    # Store manuscript and get ID
    manuscript_id = document_store.store_manuscript(manuscript_data)

    # Initialize state with only required starting fields
    return {
        "manuscript": state["manuscript"],
        "title": state["title"],
        "synopsis": state["synopsis"],
        "manuscript_id": manuscript_id,
        "status": "started",
        "current_team": "research",
        "stage_progress": {"intake": 1.0},
        # Initialize optional fields as None
        "target_audience": None,
        "research_insights": None,
        "analysis_results": None,
        "settings": None,
        "characters": None,
        "subplots": None,
        "draft_version": 0,
        "story_analysis": None,
        "continuity_issues": None,
        "style_analysis": None,
        "final_review": None,
        # Initialize team status tracking
        "research_status": {
            "market_research": False,
            "content_analysis": False,
            "audience_analysis": False
        },
        "writing_status": {
            "world_building": False,
            "character_development": False,
            "draft": False
        },
        "editorial_status": {
            "review": False,
            "polish": False,
            "quality": False
        }
    }


def conduct_market_research(state: Dict[str, Any]) -> Dict[str, Any]:
    """Conduct market research on publishing trends and target audience."""
    logger.info(f"Conducting market research for manuscript {state['manuscript_id']}")

    agent = MarketResearcher()
    result = agent.research_market(state["manuscript_id"], state["title"])

    # Update state with research insights
    new_state = state.copy()
    new_state["research_insights"] = result.get("research_insights", {})
    new_state["target_audience"] = result.get("target_audience", {})
    new_state["current_state"] = "analysis"
    new_state["message"] = (
        "Completed market research on publishing trends and target audience."
    )
    new_state["stage_progress"] = {**state.get("stage_progress", {}), "research": 1.0}

    # Store the research insights in MongoDB Atlas Vector
    document_store = DocumentStore()
    doc = Document(
        page_content=str(result.get("research_insights", {})),
        metadata={
            "type": "market_research",
            "manuscript_id": state["manuscript_id"],
            "title": state["title"],
        },
    )
    document_store.db.store_documents_with_embeddings("research", [doc])

    return new_state


